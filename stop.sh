#!/bin/bash

BASE_DIR=`pwd`
SERVER_DIR=${BASE_DIR}/server 
CLIENT_DIR=${BASE_DIR}/client 

# ############################################################################ #
# STOP CLIENT                                                                 #
# ############################################################################ #
if [ -d "${CLIENT_DIR}/dist" ]; then
  cd ${CLIENT_DIR}/dist
  ./stop.sh
fi

# ############################################################################ #
# STOP SERVER                                                                 #
# ############################################################################ #
cd ${SERVER_DIR}
./stop.sh